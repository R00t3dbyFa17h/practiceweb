## Strings are sequences of characters
name = "Harry"
## this is how you access each character in a string
print(name[0])  # H
print(name[1])  # a
print(name[2])  # r
print(name[3])  # r
print(name[4])  # y 
## also known as indexing 
# when you want to store mutiple names in a single sequence
name = ["Harry", "Ron", "Hermione"]

print(name[0])  # Harry
print(name[1])  # Ron
print(name[2])  # Hermione
